/*! \file TechKB.h
 *
 *	\author   Andreas Pott
 *
 *  \dependency
 *		none
 *
 *  \source
 *		the numeric values are collected from the technical documentation of the
 *      component manufacturer.
 *
*   \remark
 *  	The content and structure of the techdb is still alpha state!
 * 
 *  This file is understood to be the technology parameter database. Therefore, 
 *  it containts data structs and const value arrays that represent a data base
 *  with mechanical engineering components, i.e. motors and gearboxes. If the 
 *  amount of data is significantly increase it might make sence to set up a
 *  relational data base (such as MySQL), but for only a couple of tables each 
 *  with some hundred entities, some structs and static constants are efficient
 *  enough for our purpose.
 *
 *  In the following tables the technical paramters of motors such as torque, velocity,
 *  and mass are stored for all motors of a certain series.
 */

